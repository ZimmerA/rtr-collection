int main()
{
    glfwInit();

    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 4);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 6);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);
    glfwWindowHint(GLFW_RESIZABLE, GL_FALSE);

    GLFWwindow *window = glfwCreateWindow(800, 600, "Cube", NULL, NULL);
    glfwMakeContextCurrent(window);

    if (window == NULL)
    {
        std::cout << "Failed to create GLFW window" << std::endl;
        glfwTerminate();
        return -1;
    }

    if (!gladLoadGLLoader((GLADloadproc) glfwGetProcAddress))
    {
        std::cout << "Failed to initialize OpenGL context" << std::endl;
        return -1;
    }

    glViewport(0, 0, 800, 600);
    glEnable(GL_CULL_FACE);
    glEnable(GL_DEPTH_TEST);

    VertexBuffer cubeVbo;
    VertexArray cubeVao;

    cubeVao.bind();
    cubeVbo.buffer(sizeof(cubeVertices), cubeVertices, GL_STATIC_DRAW);
    cubeVao.vertexAttribPointer(ATTRIBUTE_POSITION, 3, GL_FLOAT, GL_FALSE, sizeof(float) * 8,
                                (const GLvoid *) 0);
    cubeVao.vertexAttribPointer(ATTRIBUTE_NORMAL, 3, GL_FLOAT, GL_FALSE, sizeof(float) * 8,
                                (const GLvoid *) (sizeof(float) * 3));

    // Setup shader
    Shader vs(GL_VERTEX_SHADER);
    vs.attachSourceFromFile("src/basic/02_Cube/cube.vs");
    Shader fs(GL_FRAGMENT_SHADER);
    fs.attachSourceFromFile("src/basic/02_Cube/cube.fs");

    ShaderProgram program;
    program.attachShader(&vs);
    program.attachShader(&fs);
    program.linkProgram();
    program.use();

    GLint mvpUniformLoc = glGetUniformLocation(program.handle, "mvp");
    GLint modelUniformLoc = glGetUniformLocation(program.handle, "model");

    // Setup mvp
    glm::mat4 perspective = glm::perspective(70.0f, 800.0f / 600.0f, 0.01f, 100.0f);
    glm::mat4 model = glm::rotate(glm::translate(glm::mat4(1), glm::vec3(-1.0f, 0.0f, -9.0f)), 45.f,
                                  glm::vec3(0, 1, 0));

    while (!glfwWindowShouldClose(window))
    {
        glfwPollEvents();

        glClearColor(0.2f, 0.3f, 0.3f, 1.0f);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
        program.use();
        glm::mat4 mvp = perspective * model;
        glUniformMatrix4fv(mvpUniformLoc, 1, GL_FALSE, glm::value_ptr(mvp));
        glUniformMatrix4fv(modelUniformLoc, 1, GL_FALSE, glm::value_ptr(model));
        cubeVao.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);

        glfwSwapBuffers(window);
    }

    glfwTerminate();

    return 0;
}
